package com.jarry.jayvoice.bean;

import java.io.Serializable;

import cn.bmob.v3.datatype.BmobFile;

/**
 * unit album
 * 
 * @author wangfj
 */
public class Album extends BaseBean {
	
	private static final long serialVersionUID = 8517633545835124349L;
	
	
	/**
	 * link to the cover of the album
	 */
	private BmobFile image;
	
	private BmobFile big_image;
	
	/**
	 * cartoon img
	 */
	private BmobFile cartoonImg;
	
	/**
	 * name of the album
	 */
	private String name;
	
	/**
	 * Rating of the album
	 */
	private double rating;
	
	/**
	 * Display name of the artist.
	 */
	private String artistName;
	
	private int num;
	
	private String publishTime;
	
	private String detailUrl;
	
	private String language;
	
	private String desc;
	
	private Singer singer;
	
	
	public BmobFile getImage() {
		return image;
	}
	public void setImage(BmobFile image) {
		this.image = image;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getArtistName() {
		return artistName;
	}
	public void setArtistName(String artistName) {
		this.artistName = artistName;
	}
	public void setRating(double rating) {
		this.rating = rating;
	}
	public double getRating() {
		return rating;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getPublishTime() {
		return publishTime;
	}
	public void setPublishTime(String publishTime) {
		this.publishTime = publishTime;
	}
	public String getDetailUrl() {
		return detailUrl;
	}
	public void setDetailUrl(String detailUrl) {
		this.detailUrl = detailUrl;
	}
	public BmobFile getCartoonImg() {
		return cartoonImg;
	}
	public void setCartoonImg(BmobFile cartoonImg) {
		this.cartoonImg = cartoonImg;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}
	public Singer getSinger() {
		return singer;
	}
	public void setSinger(Singer singer) {
		this.singer = singer;
	}
	public BmobFile getBig_image() {
		return big_image;
	}
	public void setBig_image(BmobFile big_image) {
		this.big_image = big_image;
	}
	
}
